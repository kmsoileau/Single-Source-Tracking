	public double lower;
	public double upper;
	public int contents;
	
	public Bin(double lower, double upper)
	{
		this(lower,upper,0);
	}
	
	public Bin(double lower, double upper, int contents)
	{
		this.lower=lower;
		this.upper=upper;
		this.contents=contents;
	}
	
	public void clear()
	{
		this.contents=0;
	}
	
	public String excelOut()
	{
		String ret="";
		ret+=this.lower+"\t"+this.upper+"\t";
		ret+="\t"+this.contents+"\n";
		return ret;
	}
	
	public String toString()
	{
		String ret="(";
		ret+=this.lower+","+this.upper+"$";
		ret+=":"+this.contents+"\n";
		return ret;
	}
